def salery(self, base_salery, a):
        """ compute salery 
            >>> @param:base_salery  -> [float] base salery for employee
            >>> @param:a            -> [int]   description of a This is a 
                                        very description of a
            >>> @return:            -> [float]  return gross salery for 
                                        employee
        """

        return base_salery + a
